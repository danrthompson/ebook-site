						<h5>What Is Crowdfunding?</h5>
						
						<a href="http://bit.ly/11ONp1m"><h6>The Crowdfunding Bible</h6></a>
						<p id="post-paragraph"> The Crowdfunding Bible' ebook was written to guide you through the process of raising money for any startup or project. Valuable contents include the pros and cons of crowdfunding as well as the types of projects that can use crowdfunding as a valuable investment source.</p>

						<a href="http://bit.ly/121GBMB"><h6>A Kickstarter's Guide to Kickstarter</h6></a>
						<p id="post-paragraph"> An ebook (free PDF edition) written on how to launch a Kickstarter campaign. This guide contains everything the author wishes he knew before he started his first Kickstarter campaign.</p>

						<a href="http://bit.ly/12rsUGR"><h6>The Ultimate Crowdfunding To-Do List</h6></a>
						<p id="post-paragraph"> An extensive to-do list and terrific reminder material for the basics of ideating, completing, and fulfilling a successful crowdfunding campaign.</p>

						<h5>Crowdfunding as in Industry</h5>

						<a href="http://bit.ly/11JKgDl"><h6>A Snapshot on Crowdfunding</h6></a>
						<p id="post-paragraph"> The Snapshot on Crowdfunding PDF ebook describes the phenomenon crowdfunding and explores the different business models of crowdfunding. The book examines the economic relevance of crowdfunding on an empirical basis.</p>
						
						<a href="http://bit.ly/18hnC9f"><h6>Crowdfund Investing - A Solution to the Capital Crisis Facing Our Nation's Entrepreneurs</h6></a>
						<p id="post-paragraph"> Testimony of a successful entrepreneur to the United States House of Representatives outlining the growth and effectiveness of crowdfunding and supporting financials. The article includes the framework that entrepreneurs/small businesses have to comply with for the Small Business and Entrepreneurship Council (SEC) rules.</p>

						<a href="http://bit.ly/139s60M"><h6>Crowdfunding 101</h6></a>
						<p id="post-paragraph"> A legal crowdfunding guide authored by those who pioneered the legalization of crowdfunding as an investment vehicle.</p>
						
						<h5>Kickstarter vs. Indiegogo</h5>

						<a href="http://bit.ly/17isfwZ"><h6>Crowdsourced Fundraising: Kickstarter vs. Indiegogo</h6></a>
						<p id="post-paragraph"> A comprehensive comparison of the pros and cons of Indiegogo and Kickstarter. The article cautions to plan ahead for the fees that these sites require and reminds the reader to not attempt to crowd source a project without a thoughtful outreach and marketing strategy.</p>
						
						<a href="http://kck.st/11JKE4Q"><h6>Kickstarter School</h6></a>
						<p id="post-paragraph"> An 8-step guide for launching a crowdfunding campaign, provided by Kickstarter. Lessons covered include how to define your project, how to create rewards, and how to set your goals.</p>

						<a href="http://bit.ly/1coLaGD"><h6>How To Get Your Crowdfunding Campaign To Indiegogo To The Top</h6></a>
						<p id="post-paragraph"> A guide by Wired that outlines great tips for Indiegogo (most of which are also applicable to Kickstarter). The guide includes five years of data that helps to illustrate "what matters and what doesn't" when it comes to a successful campaign strategy.</p>
